    <h2 id="5">8 Советов по успешному заполнению опросов</h2>

    <p class="paragraph">
		Прежде чем стать экспертом в платных опросах, есть некоторые вещи, которые вы должны знать.  Учитывайте эти советы и помните: терпение и настойчивость являются ключевыми качествами для получения дополнительного дохода.
    </p>

    <p class="paragraph">
		<strong style="color:#D11E21;">Снимайте</strong> заработанные деньги как можно быстрее. Опросные компании не являются банками. Нет никакой гарантии, что опросная компания не закроет сайт и скроется с вашми деньгами. Поэтому выводите деньги, которые вы заработали, как только достигнете минимальной суммы вывода.
    </p>

    <p class="paragraph">
		<strong style="color: #16D366;">Создайте</strong> электронную почту отдельно для опросов: не используйте свой личный email. Потому что ваш почтовый ящик будет перегружен от различного рода спама. Вместо этого создайте учетную запись только для опросов.  Это также поможет вам быстрее находить анкеты, среди гор писем, приходящих вам на основную почту.
    </p>

    <p class="paragraph" style="padding: 0 0 3% 0;">
		<strong style="color: #1F70C5">Проверяйте</strong> электронную почту несколько раз в день. Потому что опросы закрываются, когда достаточно людей уже ответили.
