   <h2 id="6" >Что из себя представляет пост и как его оптимизировать?</h2>

   <img class="w40" data-src="img/Post.png" alt="Post">
  <p class="paragraph">После того как вы выберете сайт,. с помощью которого будете зарабатывать, перед вами встанет задача научиться публиковать там посты. Что это такое ? В общем случае - это заголовок, описание и прикрепленные файлы. Сейчас мы рассмотрим как правильно составить заголовок и описание. Третий пункт трогать не будем, так как уже достаточно сказано.</p>

   <p class="paragraph">Самый лучший пост - это SEO - пост. В большинстве случаев на вашу новость будут натыкаться через обычный поиск, поэтому так важна оптимизация под поисковые запросы.</p>

   <ul style="margin: 3% 0 0 6%; list-style-type: disc;">
   	<li><strong>Заголовок.</strong>
		<p style="margin: 0% 0 3% 0">Заголовок должен содержать ключевые слова, по которым вашу новость будут искать. При этом чем ближе ключевое слово к началу заголовка, тем оно важнее для поисковой системы. Также заголовок должен состоять не просто из отдельных ключевых слов, а из словосочетаний.</p>
	   </li>
   	<li><strong>Описание.</strong>
		<p style="margin: 0% 0 3% 0">Самое важное здесь - это уникальность. Если вы скопируете описание из другой новости, то поисковая система всегда покажет первоисточник. В идеале ваш пост должен быть с одной стороны уникальным по информации, и с другой содержать тот же набор ключевых слов, что и самые популярные новости.</p>
	   </li>
   </ul>

