			<p id="3.1" class="paragraph"><strong>Тип устройства</strong></p>
			
			<img class="w100" data-src="img/direct_3.PNG" alt="direct_5" itemprop='url contentUrl'>
			
			<p class="paragraph">В верхней части экрана ( чуть ниже поисковой строки ), расположена группа вкладок с названиями(Все, Десктопы, Мобильные, Только телефоны, Только планшеты). Как не трудно догадаться переключаясь между этими вкладками мы можем узнать сколько раз “Поисковый запрос” вводили только пользователи компьютеров или только пользователи мобильных.</p>
			
			<p class="paragraph">Это очень удобно в тех случаях, когда целевая аудитория, интересы которой вы хотите узнать. Преимущественно пользуется только одним типом устройств. К примеру, поисковый запрос “Скачать Windows 10” выдает 30 000 показов на мобильных устройствах и 1 280 000 показов на ПК. Что естественно, ведь Windows 10 устанавливают на ПК.</p>
			
