			<ul style="margin: 3% 0 0 9%; list-style-type: upper-alpha;">
				<li>
					<p id="3.3.1" class="paragraph"><strong>Региональная популярность.</strong></p>
					
					<p class="paragraph">В правой части, окна с поисковыми запросами, есть столбец: “Региональная популярность”. Этот столбец показывает - на сколько данный поисковый запрос, в конкретном регионе, ищут чаще, чем другие.</p>
					
					<p class="paragraph">100% - означает, что запрос имеет обычную популярность, в сравнении с другим запросами по региону.</p>
					
					<p class="paragraph"><100% - пониженная популярность.</p>
					
