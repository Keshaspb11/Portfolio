	<h2 id="1" >Что такое <span style="color: red;">Y</span>andex Wordstat в 3-х словах</h2>
	
	<p class="paragraph"><span style="color: red;">Y</span>andex Wordstat - это сервис, который позволяет определить интересы пользователей</p> 

    <p class="paragraph">Суть проста, когда мы что-то ищем в интернете, то набираем определенные “Поисковые запросы”. Например, “купить iPhone”. Поисковая система Яндекс собирает статистику по всем поисковым запросам, которые вводят люди. И с помощью Яндекс Вордстат, мы можем использовать данные этой статистики.</p>
	
	<h2 id="2" >Основной функционал</h2>
	
	<p class="paragraph">
		Первое, что вам придется сделать перед началом работы с Яндекс Вордстат, это зарегистрироваться. Без этого сервис не даст вам даже чихнуть. Поэтому быстро вводим свои данные и получаем аккаунт. Либо входим через социальные сети, такие как VKontakte.
	</p>
	
	   <img class="w100" data-src="img/direct_1.PNG" alt="direct_1" itemprop='url contentUrl'>
	
	<p class="paragraph">
		После регистрации нам открываются все возможности сайта. Давайте разберемся что здесь к чему.
	</p>
	
	<p class="paragraph">
		Перед вами поисковая строка и краткое описание того, что от вас требуется. Введите любой поисковый запрос. К примеры введем “Подушка”(естественно без кавычек)
	</p>
	
	<p class="paragraph">
		Что мы видим ? Перед нами 2 столбца, в которых Яндекс показывает статистику ключевых слов.
	</p>
	
	    <img class="w100" data-src="img/direct_2.PNG" alt="direct_2" itemprop='url contentUrl'>
	
	<h3 id="2.1" style="margin: 5% 0 5% 3%; padding: 0;">Левый столбец</h3>
	
	<p class="paragraph">Это столбец который расположен слева от правого столбца. Данный столбец можно условно разделить на 2 части.</p>
	
	 <img class="w50" data-src="img/Left_Colomn.PNG" alt="direct_3" itemprop='url contentUrl'>
	
	<p class="paragraph"><strong>Первая часть</strong> состоит всего из 1 поискового запроса. Это слово или словосочетание, которое мы вводили в поисковой строке. Оно всегда стоит на первом месте.</p>
	
	<p class="paragraph"><strong>Вторая часть</strong> - это как вы уже догадались все остальные поисковые запросы левого столбца. Это поисковые словосочетания, в состав которых входит наша фраза. При этом искомый запрос может указываться в различных падежах или склонениях.</p>
	
	<p class="paragraph">В правой части левого столбца указаны цифры. Это количество показов конкретного поискового запроса в месяц. При этом совсем не значит, что люди вводили в течении прошлого месяца запрос “Подушка” 2 000 000 раз.</p>
	
	<p class="paragraph"><strong>Количество показов в месяц говорит</strong> нам - сколько раз данная фраза встречалась в запросах пользователей, отдельно или в составе с другими словами. Отсюда вытекает еще одна особенность. Число показов “Главного поискового запроса”(первая часть) - это сумма показов “Составных поисковых запросов”(вторая часть).</p>
	
	<h3 id="2.2" style="margin: 5% 0 5% 3%; padding: 0;">Правый столбец</h3>
	
	 <img class="w50" data-src="img/Right_Colomn.PNG" alt="direct_4" itemprop='url contentUrl'>
	
	<p class="paragraph">Как подсказывает нам сам Яндекс Вордстат - это столбец с похожими поисковыми запросами. Что означает эта фраза ? “Похожие” поисковые запросы - это запросы, в состав которых <strong><u>может</u></strong> не входить(или входить) “Главный” поисковый запрос, <strong>главное:</strong> данные фразы люди ищу вместе с “Главной” поисковой фразой.</p>
	
	<p class="paragraph">Например, когда вы хотите купить телефон, то вводите: “смартфон” или “купить телефон”. После этого вы решаете уточнить марку и пишете “Купить Xiaomi”, при этом вас все равно интересует покупка телефона. Так вот в данном случае запрос “Купить Xiaomi” является “Похожим поисковым запросом”.</p>
	
	<p class="paragraph">На этом моменте можно закончить с основным функционалом. Переходим к более детальному рассмотрению интерфейса Яндекс Вордстат.</p>
	
