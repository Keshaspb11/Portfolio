		<li>
			<p class="paragraph"><strong id="1.5" style="font-size: 1.2em;">5. Познакомьтесь со своим клиентом <span style="color: red;">до</span> звонка.</strong></p>
			
			<img class="w50" data-src="img/mentor_large.jpg" alt="Знакомство">
			
			<p class="paragraph">Данный совет подходит скорее для продавцов в сфере B2B, если вы делаете холодные звонки с целью продать какой-то мелкий товар, да еще и физ. лицу(к примеру мне недавно звонили и предлагали установить видео-домофон). В этом случае, вам будет сложно применить совет, обсуждаемый в данном параграфе на практике.</p>
			
			<p class="paragraph"><strong>В чем суть этого совета ?</strong> Он заключается в использовании преимуществ, Интернета. Лучше узнайте клиента и его компанию перед звонком, вместо того, чтобы пытаться продать с первого вызова, который редко дает результат. Предлагаю использовать различные сервисы для поиска информации - это позволит вам наладить доверительные отношения с абонентом.
			(Соц. сети, сайт компании)</p>
			
			<p class="paragraph">Можно даже пойти дальше и посмотреть не имеет ли ваш клиент проблем с законом. Для этого проверьте в каких судебных делах он учавствовал. Это информация ,<strong>во-первых</strong>, позволит узнать о проблемах вашего покупателя, <strong>во-вторых</strong>, даст вам понимание, а стоит ли вообще с ним связываться.</p>
		</li>
		
		<li>
			<p class="paragraph"><strong id="1.6" style="font-size: 1.2em;">6. Звоните только <span style="color:red;">потенциальным</span> клиентам</strong></p>
			
			<p class="paragraph">Люди не хотят слышать рассказ о товаре, который их не интересует. От продавцов, которые зачастую даже не знают свой товар.</p>
			
			<p class="paragraph">Еще одним методом холодных звонков повышающим вашу эффективность является составление базы данных заинтересованных клиентов. И обзванивание уже их. По меньшей мере люди на другой стороне провода не будут сразу бросать трубку.</p>
		</li>
		
		<li>
			<p class="paragraph"><strong id="1.7" style="font-size: 1.2em;">7. Спрашивайте разрешение.</strong></p>
			
			<p class="paragraph">В первые несколько секунд спросите разрешение на начало разговора и расскажите о преимуществах, которые вы дадите ему. Например: Добрый день, меня зовут Михаил я звоню из компании Nexart, мы занимаемся увеличением продаж наших клиентов, можно поговорить с вами об этом ? Поверьте, вам ответят согласием. Кроме того, т.к клиент сам дал разрешение на разговор, то будет более благосклонен к вашим идеям.</p>
		</li>
		
		<li>
			
		   <p class="paragraph"><strong id="1.8" style="font-size: 1.2em;">8. Секретари - это ценный источник информации.</strong></p>
			
			
			<img class="w50" data-src="img/secretar.jpg" alt="Секретарь">
			
			<p class="paragraph">Как правило, с первыми с кем нам удается установить контакт - это различные помощники и секретари. Да, они не управляют бизнесом напрямую. Но стоит помнить, что вы далеко не единственный, кто предлагает услуги. Секретарь может получать десятки предложений о сотрудничестве в день, каждый день.</p>
			
			<p class="paragraph"><strong>Поэтому</strong>, он знает на много больше о том, чего хочет его компания, нежели вы. Секретарь или помощник может стать ценным источником информации. А это информация пригодится вам при разговоре с человеком, отвечающим за покупки.</p>
			
			<p class="paragraph"><strong>Кроме того</strong> никогда не знаешь, кем является помощник, с которым вы разговариваете, для директора. Вдруг это родной брат, подруга или любовница.</p>
		</li>
		
		<li>
			<p class="paragraph"><strong id="1.9" style="font-size: 1.2em;">9. Ведите статистику</strong></p>
			
			<img class="w30" data-src="img/statistics-edmond.jpg" alt="Статистика">
			
			<p class="paragraph">Составьте таблицу или список, куда вы будете записывать результаты звонков за день. В ней должны быть такие столбцы как:</p>
			
			<ul style="margin: 3% 0 0 3%; list-style-type: none;" class="list_Level2">
				<li>
					<p class="paragraph"><strong>1. Имя клиента.</strong></p>
					<p style="margin: 0% 0 0 6%;">Имя вам понадобится, когда вы будете звонить в следующий раз. Это приятно удивит слушателя.(Конечно при условии, что вы будете разговаривать с тем же человеком)</p>
				</li>
				<li><p class="paragraph"><strong>2. Тип клиента.(Старый или вы уже звонили ему)</strong></p></li>
				<li>
					<p class="paragraph"><strong>3. Контакт.(номер телефона)</strong></p>
					<p style="margin: 0% 0 0 6%;">Если мы получаем контакт, то <strong>во-первых</strong>, в следующий раз вам не придется пробиваться через секретаря. А <strong>во-вторых</strong>, человек уже будет ожидать вашего звонка.</p>
				</li>
				<li><p class="paragraph"><strong>4. Email</strong></p></li>
				<li><p class="paragraph"><strong>5. Результат.</strong></p>
					<p class="paragraph">Результатом может выступать - успешное завершение сделки, отказ клиента, либо <strong>зачастую</strong> обещание связаться позже.</p>
				</li>
				<li><p class="paragraph"><strong>6. Комментарий.(это поле вы заполняете, после анализа)</strong></p></br></li>
			</ul>
			
		</li>
		
