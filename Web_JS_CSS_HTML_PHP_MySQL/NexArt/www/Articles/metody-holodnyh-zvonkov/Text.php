	<p class="paragraph">Поиск новых клиентов, методом холодного звонка - без сомнений самая страшная вещь в продажах. Страх отказа является причиной того, что многие люди боятся приближаться к трубке телефона и начинать беседу с незнакомым человеком.  Этот страх без сомнения сильно влияет на эффективность менеджера по продажам.</p>
	
	<p class="paragraph">Хорошая база телефонных номеров, несомненно лучший инструмент любого продажника, но иногда нам приходится брать в руки телефон и звонить незнакомым людям. Отсутствие входящих звонков совсем не означает, что ваша услуга никому не нужна, скорее всего о ней никто не знает. Так возьмем и исправим это положение.</p>
	
	<h2 id="1">9 методов, которые увеличат эффективность холодного звонка.</h2>
	
	<ul class="paragraph" style="list-style-type: none;">
		<li>
			<p class="paragraph" style="margin: 3% 0 0 0;"><strong id="1.1" style="font-size: 1.2em;">1. Настрой.</strong></p>
			<img class="w20" data-src="img/smile.JPG" alt="Улыбка">
			<p class="paragraph">Как бы банально это не звучало, но тот настрой с которым вы вступаете в разговор, самым прямым образом влияет на результат беседы. Если у вас плохое настроение, то голос будет тихим и вялым. Всегда помните, тон нашего голоса по телефону на порядок ниже, чем на самом деле. Если вы говорите спокойным голосом, то собеседник услышит сонливость с вашей стороны. Поэтому разговаривайте с покупателем в приподнятом настроении.</p>
		</li>
		
		<li>
			<p class="paragraph"><strong id="1.2" style="font-size: 1.2em;">2. Поставьте себе <strong style="font-size: 1.2em; color: red;">локальную</strong> цель</strong></p>
			
			<img class="w40" data-src="img/cel.jpg" alt="Цель">
			
			<p class="paragraph">Какую цель должен ставить перед собой продавец, перед каждым звонком ? Если вы ответили - "Продать товар", то вы не совсем правы. Мы звоним на нашему клиенту по телефону и как правило попадаем на секретаршу. Вот мы презентовали свой товар. Рассказали о преимуществах и выгодах. Даже отработали возражения. После чего получаем ответ - "Хорошо, мы вам перезвоним". А они редко перезванивают.</p>
			
			<p class="paragraph">При этом данный сценарий звонка весьма позитивный. Нас ведь не послали после первого же слова. И тем не менее мы не добились того результата, который поставили перед собой. В чем же дело ? Дело в постановке цели.</p>
			
			<p class="paragraph"><strong>Во-первых</strong>, секретарь вряд ли занимается вопросами покупки новых товаров. А даже если занимается, то деньги все равно будет платить генеральный директор. А он не слышал вашей презентации. Ему все равно на ваш товар. А секретарша, не сможет передать свой энтузиазм от приобретения вашего товара.</p>
			
			<p class="paragraph"><strong>Во-вторых</strong>, по телефону деньги не передашь. А значит клиент не купит ваш товар сразу.</p>
			
			<p class="paragraph">Поэтому крайне важно ставить "микро-цели". К примеру: получить телефон вышестоящего человека, который отвечает за закупки или даже с самим директором. Или договориться о встрече. Таким образом вы хоть и маленькими шагами, но приближаетесь к удачному завершению сделки.</p>
		</li>
		
		<li>
			<p class="paragraph"><strong id="1.3" style="font-size: 1.2em;">3. Говорите о том, что получит клиент, а не о том, чего бы хотели вы.</strong></p>
			
			<p class="paragraph">Используйте при звонке такие фразы как: "Ваша компания получит","Это повысит ваши доходы в ... раз", "Вот как это повлияет на вас.."</p>
			
			<p class="paragraph">И напротив откажитесь от использования таких фраз как: "Мы бы хотели(сотрудничать с вами)",понятное дело, что вы хотите, иначе не звонили бы, но зачем мне сотрудничать с вами. "Я бы хотел предложить вам.."</p>
			
			<p class="paragraph">Естественно, что так или иначе вам придется предлагать товар, но сначала заинтересуйте покупателя, теми выгодами, которые он получит, а уже потом предлагайте.</p>
		</li>
		
		<li>
			<p class="paragraph"><strong id="1.4" style="font-size: 1.2em;">4. Как должен выглядеть скрипт звонка.</strong></p>
			
			<img class="w50" data-src="img/script.jpg" alt="Сценарий">
			
			<p class="paragraph">Если вы новичок, то так или иначе вам понадобится скрипт, по которому вы будете разговаривать с клиентом. Почему нужен скрипт ? Во-первых, это придает уверенности. Куда легче находить ответы на вопросы покупателя, когда половина из них уже записана у тебя. Во-вторых, при звонке вы представляете не только себя. Вы представляете компанию, а значит ваш разговор должен соответствовать некоторым стандартам. В-третьих, вы всегда должны видеть перед собой цель разговора, а также на каком этапе вы находитесь. Ведь ваша цель все таки продать, а не мило побеседовать.</p>
			
			<p class="paragraph">Так вот, если вы ищите скрипт на просторах интернета или составляете собственный, то составьте сценарий из <strong>действий</strong>, а не из фраз.</p>
			
			<p class="paragraph"><strong>Пример неэффективного скрипта:</strong></p>
			
			<p class="paragraph">
				<div style="margin: 3% 0 0 6%;">
					<p><strong>Вы:</strong> Добрый день, компания "Двери в дом", отдел продаж, Михаил.</p>
					<p><strong>Клиент:</strong> Здравствуйте.</p>
					<p><strong>Вы:</strong> Слышал, что вам нужны двери. Мы можем предоставить для вашей компании двери отличного качества. Наши двери смогут защитить ваши сбережения и т.д(презентация довольно абстрактная, но для примера сойдет).</p>
				</div>
			
			<p class="paragraph"><strong>Пример эффективного скрипта:</strong></p>
			
			<div style="margin: 3% 0 0 6%;">
					<p><strong>Вы:<</strong>Приветствие, название компании, отдел, ваше имя <strong>></strong> + пара примеров.</p>
					<p><strong>Клиент:</strong> Здравствуйте.</p>
					<p><strong>Вы:</strong>Презентовать товар, рассказав о (таких то преимуществах). Показать выгоду для покупателя (список заранее подготовленных выгод).</p>
			</div>
				  
			</p>
		</li>

