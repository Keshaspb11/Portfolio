					<p class="paragraph"><strong>Черты характера</strong>: дружелюбный и разговорчивый. Соглашается на встречи даже тогда, когда нет конкретной необходимости. Отзывчивый. Ненавидит конфликты и говорить "нет.”</p>
					
					<p class="paragraph">Стратегия презентации:</p>
					<ul class="paragraph">
						<li>1. Повторите, то что сказал вам покупатель, тем самым проверяя его потребности и цели.</li>
						<li>2. Наблюдайте за несоответствием языка теля со словами, которые вам говорят. Реагируйте если что-то заметите.</li>
						<li>3. Постройте презентацию таким образом, чтобы клиенту было легко НЕ согласиться. Это расслабит клиента, после чего он может рассказать вам его истинные возражения.</li>
					</ul>
				</li>
				
				<h4 class="paragraph">2. Аналитический покупатель</h4>
				<li class="paragraph">
					<p class="paragraph">Как видно из названия, такие покупатели любят анализировать. Обычно они встречаются в организациях, занимающихся технической или финансовой деятельностью.</p>
					
					<p class="paragraph"><strong>Черты характера</strong>: Скептически относятся к расплывчатым заявлениям и общим или чрезмерно громким утверждениям. Работает только с  фактами. Любит анализировать и сравнивать. Не любит рисковать и требует времени для принятия решения.</p>
					
					<p class="paragraph">Стратегия презентации:</p>
					<ul class="paragraph">
						<li>1. Постройте свою презентация логически и методично.</li>
						<li>2. Предоставьте факты и примеры по ходу рассказа.</li>
						<li>3. Сведите эмоции к минимуму и придерживайтесь фактов.</li>
						<li>4. Подготовьте источники.(на случай, если покупатель захочет проверить, сказанное вами.)</li>
						<li>5. Используйте истории успеха.</li>
					</ul>
				</li>
				
				<h4 class="paragraph">3. Импульсивный покупатель</h4>
				<li class="paragraph">
					
					<p class="paragraph"><strong>Черты характера</strong>: напористый. Постоянно переключает внимания с предмета на предмет. Может быть импульсивным, принимает решения быстро.</p>
					
					<p class="paragraph">Стратегия презентации:</p>
					<ul class="paragraph">
						<li>1. Разбейте презентация на небольшие отрывки(длинное обсуждение утомит вашего клиента).</li>
						<li>2. Каждый отрывок, должен быть различным способом взаимодействия(история, вопросы), но вести к одной цели.</li>
						<li>3. Задавайте вопросы начинающиеся на "Что ... ?" и избавьтесь от "Как ... ?".</li>
						<li>4. Коротко и часто подводите итоги.</li>
					</ul>
				</li>
				
				<h4 class="paragraph">4. Смелый покупатель</h4>
				<li class="paragraph">
					
					<p class="paragraph"><strong>Черты характера</strong>: напористый. Не боится высказывать свое мнение или ожидания. Постоянно смотрит за временем. Любит все контролировать. Жесткий переговорщик.</p>
					
					<p class="paragraph">Стратегия презентации:</p>
					<ul class="paragraph">
						<li>1.  Начните с того, что является наиболее важным или представляет интерес для вашего клиента. Это может быть ключевой вопрос или польза, которую получит клиент.</li>
						<li>2. Сосредоточьтесь на фактах и результате.</li>
						<li>3. Будьте напористым. Не уступайте клиенту иначе он задавит вас свои авторитетом.</li>
						<li>4. Поддерживайте зрительный контакт.</li>
						
