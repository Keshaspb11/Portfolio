			<h2 id="1">Марк Уэйшек</h2>
			
			<img class="w40" data-src="/Articles/mery-po-uvelicheniyu-prodazh/img/marc-wayshak.jpg" alt="Марк Уэейшек специалист по продажам">
			
			<p class="paragraph">Компания HubSpot провела исследование, на предмет того, как часто продавцы  из сферы оптовой и розничной торговли заключают сделки с клиентами. И какие факторы влияют на продажу. Вот к каким показателям они пришли. В таких отраслях, как искусство, развлечения, бизнес, интернет и телекоммуникации продавцы в среднем заключали сделки в 20%. То есть, из 100 клиентов, с 20 был подписан договор.</p>
			
			<p class="paragraph">С другой стороны в таких отраслях как биотехнологии, страхование, гостиничный бизнес и маркетинг сделки совершались в 15%, а иногда даже в 11%. Разница почти в 100%(продавцы из первой группы отраслей закрывали продажу вдвое чаще продавцов из второй группы.)</p>
			
			<p id="1.1" class="paragraph">Вот к каким мерам нужно присмотреться по мнению Марка, чтобы оказаться среди первой группы продавцов:</p>
			
			<ul class="paragraph">
				<h3 class="paragraph">1. Перестаньте сразу предлагать товар покупателю.</h3>
				<li class="paragraph">
					
					<p class="paragraph">Есть множество компаний, который продают идентичные товары. И у каждой есть свои менеджеры по продажам и консультанты, которые пытаются убедить покупателя приобрести их товар. Точно такой же как и в соседнем магазине. <strong>Вместо этого</strong> сфокусируйтесь на рекламе вашей фирмы. Расскажите покупателю о выгодах, которые он получит, если приобретет товар именно у вас, а не в соседнем магазине.</p>
					
					<p class="paragraph">Сосредоточьтесь на решении проблем покупателя, и объясните для чего ему нужна та или иная вещь. Какой цели он добьется, если купит ее. При таком разговоре, покупатель будет относиться к вам более снисходительно, ведь вы решаете его проблемы, а не просто пытаетесь продать.</p>
				</li>
				
				<h3 class="paragraph">2. Узнайте наиболее важные проблемы клиента.</h3>
				<li class="paragraph">
					
					<p class="paragraph">Изучите самые насущные проблемы клиентов. Это не только поможет вам лучше понять их, но и добавит в ваш арсенал несколько интересных вопросов, которые вы сможете задать вашему покупателю.</p> 
					
					<p class="paragraph">К примеру, ваша услуга - это "Прыжки с парашютом". Какая самая большая проблема клиента ? Их всех заботит выживут ли они после). В таком случае, когда люди будут звонить вам(в процессе выбора компании), задайте им вопрос - "Как вы считаете, стоит ли немного переплатить, за гарантию вашей полной безопасности во время прыжка ?" - этим вопросом вы не только аргументируете вашу цену, но и показываете свое отношение к клиенту.</p>
				</li>
				
				<h3 class="paragraph">3. Предложите клиенту несколько вариантов</h3>
				<li>
					
                    <p class="paragraph">Обычно в коммерческом предложении на рассмотрение клиента компании предлагают всего один вариант. И тем самым они упускают гигантские возможности.</p>

                    <p class="important_color">Вместо одного варианта предложите три, чтобы потенциальному клиенту было из чего выбирать. Первый вариант — предложение базового, низкого уровня, но позволяющее достичь результатов, которые необходимы клиенту. Третий вариант должен быть гораздо лучше того, что хотел клиент. А вариант №2 — чем-то средним.</p>

                    <p class="paragraph">Предлагая такой выбор, вы достигаете двух важных для себя целей. Во-первых, вы даете клиенту возможность подумать, какой вариант выбрать. При этом вы устраняете его необходимость обратиться к вашим конкурентам за другими вариантами. Во-вторых, всегда есть шанс, что клиент выберет вариант номер два или три, которые для него более полезны и требуют больших вложений.</p>
					<a class='detail paragraph' href="https://kontur.ru/articles/1374">Источник(5 пункт)</a>
				</li>
				
				<h3 class="paragraph">4. Определите цели клиента и их ценность</h3>
				<li>
					<p class="paragraph">Узнайте, какой цели хочет добиться клиент. Это не только даст вам возможность предложить ему наиболее подходящее решение его проблемы, но и подчеркнет вас как эксперта.</p>
					
					<p class="paragraph">После того как цель определена, задайте покупателю вопрос по типу "Какую прибыль вы получите, в случае достижения цели ?". Если покупатели ответят сами на этот вопрос, то вы сделаете цену вашего продукта незначительной по сравнению, с суммой будущих доходов. Это называется формирование ценности.</p>
				</li>
				<h3 class="paragraph">5. Вовлеките всех лиц принимающих решения</h3>
				<li>					
					<p class="paragraph">И помните, бывают случаи когда директора компании спрашивают совета у своих подчиненных по вопросам покупки. А если вы в течении всей презентации даже не обратили внимания, на этих подчиненных, значит они не заинтересованы в этой покупке, что вряд ли будет способствовать успешному завершению продажи.</p>
				</li>
			</ul>
		</li>
		
