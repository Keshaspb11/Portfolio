		<p class="paragraph">Умение слушать является важным навыком продаж. Мы все это знаем ! Также мы знаем что продавцов нельзя назвать хорошими слушателями. Поэтому я хочу дать вам 3 волшебных совета, как правильно слушать.</p>
		
		<ul id="6.1" class="paragraph">
			
			<h3>1. Не просто слушайте. Изучайте значения слов.</h3>
			
			<li class="paragraph">
				<p>К примеру, Если клиент говорит, что рассматривает возможность смены поставщика в следующем году. Продавец слышит "Смена поставщика" и начинает презентовать свой товар. Но какое ключевое слово. Правильно - "Рассматривает"</p>
				</br>
		        <p>Спросите клиента: почему вы рассматриваете этот вопрос? Почему только рассматривают, а не решают. При каких обстоятельствах они начнут действовать более решительно ? Таким образом вы сможете получить более полную картину и превзойти конкурента.</p>
			</li></br>

            <h3>2. Пауза</h3>
			
			<li class="paragraph">
				<p>После того как клиент ответил на важный вопрос сделайте паузу. Большинство людей посчитают, что они должны заполнить тишину. И почти всегда заполнят ее важной информацией, важной для вас.</p>
			</li></br>
             <h3>3. Молчите, пока не настанет время</h3>
			
			<li class="paragraph">
				<p>Я никогда не забуду эту встречу, когда команда руководителей забрасывала друг друга словами, каждый был полон решимости высказать свое мнение.  Кроме одного.  Он сидел молча.  И когда все мы закончили, сказал: «Мне нужно сказать три вещи…» Он поделился своими тремя идеями менее чем за пять минут.  Но его вклад был более важен, чем два часа предыдущей болтовни.</p>
				</br>
