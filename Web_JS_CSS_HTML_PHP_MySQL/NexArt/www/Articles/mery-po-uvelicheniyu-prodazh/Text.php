					<p><strong>Продавец:</strong> Вы интересуетесь расширенной гарантией ?</p>
					<p><strong>Заказчик</strong>: Есть ли у вас синий цвет ?</p>
					<p><strong>Продавец:</strong> Вам нравится синий ?</p>
					<p><strong>Заказчик</strong>: Почему ты такой надоедливый ?</p>
					<p><strong>Продавец:</strong> Вам что не нравятся надоедливые продавцы ?</p>
					</br>
                    <p>Если вам задают вопрос, ответьте на него. Если хотите что-то продать, то делайте о чем просит ваш клиент.</p>
		        </li>
