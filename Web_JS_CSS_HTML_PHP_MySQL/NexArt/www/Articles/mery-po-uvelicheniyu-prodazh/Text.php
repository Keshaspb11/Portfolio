			<p class="paragraph">Повторение уже сказанного может быть мощной фишкой в арсенале продавца. Вы должны постоянно быть уверены в том, что клиенты запомнили то о чем вы им рассказываете. Часто бывает полезно повторить преимущества, конкурентные отличия или даже вопросы и слова клиента. Однако помните, что черезмерное повторение может и навредить.</p>
			
			<h3 id="2.1" class="paragraph">4 вида покупателей и стратегия презентации</h3>
			
