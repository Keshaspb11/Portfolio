			<h2 id="7" ><strong>7. Диета Scardale</strong></h2>
			
			<img class="w50" data-src="img/skaredail.jpg" alt="skaredale">
			
			<p class="paragraph">Scardale имеет две фазы. <strong>Первая</strong> длится 14 дней, в течении которой вы сбрасываете вес выполняя правила, указанные ниже. <strong>Вторая</strong> фаза предназначена для сохранения достигнутого веса. Если выполнить только первый этап, то после прекращения диеты вы можете набрать даже больше веса чем было.</p>
			
			<p class="paragraph"><strong>Правила установленные доктором Scardale:</strong></p>
	
	        <ul style="margin: 3% 0 0 9%; list-style-type: decimal;">
	        	<li>Запрещается алкоголь. Допустимые напитки: кофе, обычный или без кофеина, чай, газированная вода и безалкогольные диетические напитки.</li>
	        	<li>Разрешается съесть морковь и сельдерей между приемами пищи</li>
	        	<li>Разрешается мясо курицы и индейки без кожи</li>
	        </ul>
			
			<p class="paragraph"><strong>Сохранение веса </strong>.На этой стадии изменяются несколько правил. Во-первых, можно есть сельдерей и морковь без ограничений, и рекомендуется употреблять хлеб (2 ломтика в день максимум). Нельзя готовить яйца и мясо <u>с использованием масла</u>, рекомендуется готовить в духовке или на гриле. Фрукты и овощи можно употреблять без ограничений.</p>
			
			<p class="paragraph"><strong>На обеих фазах запрещено:</strong>сладкие десерты, шоколад и конфеты, сахар, цельное молоко, масло, макаронные изделия и майонез.</p>
		</li>
		
		<li>
			<h2 id="8" ><strong>8. Диета Перрикоун 28 дней</strong></h2>
			
			<p class="paragraph">Если вы решили следовать диете Перрикоун, необходимо употреблять продукты в следующем порядке: сначала белки, далее волокна и, наконец, углеводы. Ниже вы найдете пример меню:</p>
	
	        <ul style="margin: 3% 0 0 9%; list-style-type:disc;">
	        	<li><strong>Завтрак:</strong> омлет с тремя яйцами, полстакана овсяной каши, ломтик дыни или свежих ягод. Не употреблять соки, кофе или хлеб.</li>
	        	<li><strong>Обед:</strong> 110 - 170 грамм свежего лосося на гриле, 2 тарелки салата из  зелени (можно заправить оливковым маслом), кусочек дыни, треть стакана свежих ягод, полстакана воды.</li>
	        	<li><strong>Полдник:</strong> 170 грамм йогурта, треть чашки ягод, стакан воды.</li>
				<li><strong>Ужин:</strong> 110 - 170 грамм свежего лосося на гриле. 2 тарелки салата, с оливковым маслом, кусочек дыни и 1/3 стакана свежих ягод, стакан воды.</li>
	        </ul>
		</li>
		
