	<p class="important_color" ><strong>Милые девушки</strong>, вам не нужно худеть. Вы и так прекрасны. Но если вы все таки решили сбросить пару килограммов, то я помогу вам с этим.</p>
	
	<p class="important_color" ><strong>Мужчины</strong>, если вы зашли на эту страницу, значит на то была серьезная причина. Видимо вам действительно нужно похудеть. Читайте дальше и узнаете как.</p>
	
	<p class="paragraph">Ни для кого не секрет, что для того, чтобы сбросить вес не достаточно просто ходить в спортзал. Обязательно нужно правильно питаться. Какими бы красивыми словами вы не пользовались: "Здоровое питание", "Спортивное питание", "Питание для похудения", все это сводится к одному короткому слову <strong>"Диета"</strong></p>
	
	<p class="paragraph">Но не стоит опускать руки. Диета совершенно не означает голодание. Понятное дело, вам все равно придется ограничить себя в еде, тем не менее совершенно не обязательно ограничивать себя во вкусной еде.</p>
	
	<p id="0" class="paragraph">И так после небольшого вступления давайте рассмотрим что нужно кушать чтобы похудеть в домашних условиях.</p>
	
