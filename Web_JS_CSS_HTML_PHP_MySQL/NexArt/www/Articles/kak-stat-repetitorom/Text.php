	<h2 id="3">Сложности, с которыми вы столкнетесь со временем.</h2>
	
	<p class="paragraph">
		Вы собираетесь представлять услуги для людей, за которые они вам будут платить деньги. Наше государство подозрительно относится к людям, у которых внезапно появляются деньги, которых у них быть не должно. Поэтому, если вы решите на постоянной основе стать репетитором, то рано или поздно вам придется зарегистрировать ИП. Стать <strong>И</strong>ндивидуальным <strong>П</strong>редпринимателем.
	</p>
	
	<p class="paragraph"><strong>В этом есть как свои плюсы так и минусы.</strong></p>
	
	<p class="paragraph"><strong>К плюсам</strong> можно отнести в первую очередь, статус. Действительно, кого бы вы наняли с большей охотой официально работающее юридическое лицо или обычного человека ? Во-вторых, вам больше не надо будет бояться визитов налоговой, т.к теперь все официально.</p>
	
	<p class="paragraph"><strong>К минусам</strong> естественно относятся различного рода отчисления в счёт государства. Начнем с того, что сама регистрация ИП стоит около 3000 рублей. Плюс налоги. При этом вы все равно будете зарабатывать больше чем, на обычной работе, т.к у ИП ниже подоходный налог. Да, да это те деньги, которые вычитаю из вашей зарплаты каждый месяц, обещая выплатить на пенсии.</p>

