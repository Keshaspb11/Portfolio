	<h2 id="4">Как записать видео с экрана компьютера без сторонних приложений.</h2>
	
	 <p class="paragraph">Если вы не хотите загружать дополнительное программное обеспечение, существует возможность, записи видеороликов при помощи уже установленных на ваш компьютер программ.</p>
	
	 <h3 id="4.1" class="headline" style="margin: 6% 0 3% 3%;  font-size: 2em;"><span style="color: #E51C08;">Power</span> Point</h3>
	
	 <img class="w60" data-src="img/powerpoint.jpg" alt="PowerPoint" >
	
	 <p class="paragraph"><strong>Шаг 1:</strong> перейдите на вкладку <strong>“Вставка”</strong> и выберите пункт <strong>“Запись экрана”</strong>.</p>
	
	 <p class="paragraph"><strong>Шаг 2:</strong> нажмите кнопку <strong>“Выбор области”</strong> , чтобы выбрать конкретную область экрана, которую хотите записать. Если вы хотите записать весь экран, нажмите клавиши <strong>Windows, Shift и “F”</strong>.</p>
	
	 <p class="paragraph"><strong>Шаг 3:</strong> нажмите кнопку <strong>“Записать”</strong>, “или одновременно нажмите клавиши <strong>Windows, Shift и “R”</strong></p>
	
	 <p class="paragraph">После того как вы закончите, можно сохранить ролик отдельным файлом. Инструменты редактирования и настройки сильно ограничены, но это отличный вариант для быстрой записи, особенно если вы делаете это для вставки в презентацию. Да, и не забудьте про средства редактирования, которые  предлагает PowerPoint, такие как - изменить форму видео, добавить рамку и добавить эффекты, тени, свечение и многое другое.</p>
	
	
